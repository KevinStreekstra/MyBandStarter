<h1 class="header">Geschiedenis van Ajax</h1>

<div class="container">
        <h3>Ontstaan van de club</h3>
        <p>Een aantal vrienden richtte in 1894 een voetbalclub op. Ze noemden de club aanvankelijk "Union", maar doopten hem nog datzelfde jaar om naar "Footh-Ball Club Ajax" (inclusief de spelfout), naar de Griekse held. In deze periode werden in heel Nederland en vooral in Amsterdam veel voetbalclubs opgericht. De Amsterdamse voetbalbond stelde, om chaos te vermijden strikte regels op, de club kon hier niet aan voldoen en in 1896 ging de club praktisch ter ziele. Vier jaar later besloot Floris Stempel samen met Han Dade en Carl Bruno Reeser wederom een poging te wagen en op 18 maart 1900 werd Ajax opgericht in café "Oost-Indië" aan het begin van de Kalverstraat bij de Dam, op de plek waar tot 2012 muziekzaak Fame was gevestigd. Floris Stempel werd de eerste voorzitter van de club, waarvan de naam ditmaal wél goed gespeld werd. Om verwarring met een gelijknamige voetbalclub uit Leiden (die inmiddels bekend is als Ajax Sportman Combinatie) te vermijden werd later "Amsterdamsche" nog voor de naam gezet. In 1902 werd Ajax toegelaten in de derde klasse, waarna het direct promoveerde naar de tweede klasse. In 1908 werd gefuseerd met derdeklasser AFC Holland (opgericht in 1899) die opging in Ajax. Vier spelers van het oude Holland waren belangrijk in het latere succes in 1911.</p>
    </div>

    <div class="container2">
        <h3>Joodse achtergrond</h3>
        <p>Ajax staat nationaal en internationaal bekend om haar Joodse achtergrond. Ajax heeft verschillende bekende Joodse spelers en verenigingsmensen gehad, zoals Johnny Roeg en de in Auschwitz omgekomen Eddy Hamel. In de jaren zestig speelden Bennie Muller en mister Ajax Sjaak Swart als Joodse spelers voor de club. De laatste Joodse spelers bij Ajax waren Hedwiges Maduro, Daniël de Ridder en Ilan Boccara. Buiten het veld waren er verzorger Salo Muller en bestuurders Jaap van Praag, Michael van Praag, Michael Kinsbergen en Uri Coronel Joods.</p>
    </div>

    <div class="container3">
        <h3>Stadions</h3>
        <p>Ajax heeft in de loop der jaren op vele locaties gespeeld.Het begon allemaal op een veldje in de toenmalige gemeente Nieuwer-Amstel, nog voordat de club officieel werd opgericht (in 1893).Voor vijftien gulden per half jaar werd er een weiland gehuurd.In 1896 werd het veldje bij de gemeente Amsterdam gevoegd.In 1900 verhuisde de club naar Amsterdam-Noord, waar tot 1907 tussen de weilanden werd gespeeld.In 1907 nam Ajax intrek in het eerste voetbalstadion van Amsterdam, in de volksmond "Het Houten Stadion" genoemd.Het stadion lag dichter bij het centrum dan de vorige locaties, maar er ontbraken nog steeds tribunes (die er pas in 1911 kwamen), kleedkamers en stromend water.Het stadion bleek toch te klein te zijn: na vier opeenvolgende landstitels in de jaren dertig werd er verhuisd naar Stadion De Meer.Dit stadion zou het meest legendarische stadion uit dit rijtje worden: er speelden voetballers als Johan Cruijff, Sjaak Swart en Marco van Basten.De gemeente Amsterdam wilde bouwen in het geannexeerde Watergraafsmeer, dus liet Ajax een stadion bouwen in Betondorp.Het stadion mocht maximaal 300.000 gulden kosten, en zelfs de spelers betaalden mee.De openingswedstrijd was op 9 december 1934 tegen Stade français, een wedstrijd die met 5-1 werd gewonnen.Het duurde tot 1971 tot er lichtmasten werden geplaatst.In 1996 vertrok Ajax, na er 62 jaar gespeeld te hebben, uit De Meer.</p>
