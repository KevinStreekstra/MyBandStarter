<div class="feed2">
    <p>Marcel Keizer is aangesteld als hoofdtrainer van het eerste elftal van Ajax. Hij tekent op korte termijn een contract dat ingaat op 1 juli 2017 en een looptijd heeft van twee jaar, tot en met 30 juni 2019. In de overeenkomst is een optie voor nog een jaar opgenomen.

        Keizer (Badhoevedorp, 15 januari 1969) was het afgelopen seizoen als trainer verantwoordelijk voor Jong Ajax. Met het beloftenelftal pakte de oefenmeester een periodetitel en eindigde het team op de tweede plaats in de Jupiler League. Voor zijn komst naar Amsterdam was hij werkzaam als hoofdtrainer van FC Emmen. Op 13 februari 2016 werd hij aangesteld als hoofdtrainer van SC Cambuur Leeuwarden, waar hij eerder als technisch manager actief was. Van 2012 tot 2014 was Keizer hoofdcoach van SC Telstar.

        Als speler was Keizer actief binnen de jeugdopleiding van Ajax en debuteerde op 10 april 1988 in het eerste elftal. In totaal speelde hij vier officiële wedstrijden in de hoofdmacht van Ajax. In 1989 vertrok Keizer naar Cambuur Leeuwarden. Via De Graafschap en FC Emmen sloot hij zijn loopbaan als speler in 2002 af, waarna hij zijn trainerscarrière begon.

        Algemeen directeur Edwin van der Sar: ,,Vanaf het moment dat de kans bestond dat wij op zoek moesten naar een andere trainer, was Keizer voor het Technisch Hart de geschikte kandidaat. Het past bij Ajax om eerst intern te kijken naar opvolging, in het verleden zijn vaker hoofdtrainers uit de eigen organisatie aangesteld. De positie van hoofdcoach kwam eerder vrij dan verwacht. Vervolgens zijn wij de gesprekken gestart binnen het Technisch Hart en de directie. En daarna met de RvC en natuurlijk met Marcel zelf. Dat was allemaal positief waardoor we een nog beter gevoel hebben gekregen bij de keuze voor hem. Hij kent de spelers, de organisatie en staat achter de filosofie van de club. Bovendien heeft hij het bij de beloften het afgelopen seizoen, met een duidelijke speelwijze, uitstekend gedaan. Met goede en gevarieerde trainingen heeft hij de spelers zichtbaar beter gemaakt.”  </p>
